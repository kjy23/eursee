def process_m3u_file(input_file, output_file):
    with open(input_file, 'r', encoding='utf-8') as f:
        content = f.read()

    blocks = content.strip().split("#EXTINF")
    updated_blocks = []

    for block in blocks:
        if not block.strip():
            continue
        updated_block = update_extinf_block("#EXTINF" + block)
        updated_blocks.append(updated_block.strip())

    with open(output_file, 'w', encoding='utf-8') as f:
        f.write("#EXTM3U\n\n")
        f.write("\n\n".join(updated_blocks))

    print(f"üéâ Â§ÑÁêÜÂÆåÊàêÔºåËæìÂá∫Êñá‰ª∂‰∏∫: {output_file}")

